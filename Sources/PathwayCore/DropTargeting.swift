import Foundation

/// Куда упадут перетаскиваемые файлы в списке.
///
/// Вынесено из вью, потому что здесь легко ошибиться с индексом строки:
/// AppKit передаёт row = -1 при броске на пустую область, и обращение
/// к элементу массива по такому индексу роняет приложение.
public enum DropTargeting {
    /// Папка-получатель для строки под курсором.
    ///
    /// - Parameters:
    ///   - row: строка под курсором; -1 означает пустую область списка.
    ///   - isOnRow: AppKit предлагает бросок «на строку», а не между строками.
    ///   - isLocalDrag: перетаскивание начато в этом же списке.
    ///   - itemAt: папка для строки, либо nil если строка не папка или её нет.
    ///   - currentFolder: открытая сейчас папка.
    /// - Returns: папка, куда копировать или перемещать; nil — бросок не принимается.
    public static func destination(
        row: Int,
        isOnRow: Bool,
        isLocalDrag: Bool,
        itemAt: (Int) -> URL?,
        currentFolder: URL
    ) -> URL? {
        // Проверка row >= 0 обязательна: AppKit передаёт -1 при броске на пустую
        // область, и обращение к элементу по такому индексу роняет приложение.
        if isOnRow, row >= 0, let folder = itemAt(row) {
            return folder
        }
        // Бросок внутри своего же списка на пустое место бессмыслен —
        // файлы уже лежат в этой папке.
        guard !isLocalDrag else { return nil }
        return currentFolder
    }
}
